    public boolean addCat(){
        nam = na.getText();
        ad = add.getText();
        it  = Integer.parseInt(item.getText());
        t  = Integer.parseInt(total.getText());
        
        
        boolean f=false;
        try {
    Connection conn=DBconnection.getConnection();
           String sql="insert into ord(cusname,address,item,total) values(?,?,?,?)";
            PreparedStatement pst=conn.prepareStatement(sql);
            
           pst.setString(1, nam);
           pst.setString(2, ad);
            pst.setInt(3,it);
             pst.setInt(4,t);
           
           int rowCount = pst.executeUpdate();
           if(rowCount>0){
               f=true;
           }else{
               f=false;
           }
             
        } catch (Exception e) {
            e.printStackTrace();
        }
        return f;
    }

